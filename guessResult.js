console.log(result11)

var result12 = null == '' // false
// == Порівнює два значення на рівність, null - це порожність а '' - це стрічка
console.log(result12)

var result13 = 3 ** (9 / 3); // 27
// Спочатку виконується дія у дужках результат ділення 3
// потім 3 підноситься до ступіня 3.
console.log(result13)

var result14 = !!'false' == !!'true' // true
// Стрічки false та true перетворються на boolean значення
// і при їх порівняні == відбувається порівняння по рівності знвчення
console.log(result14)

var result15 = 0 || '0' && 1 // 1
// || перша умова у нас повертає true і тому ми йдемо далі
// && у нас зліва і з права - true тому ми пройшли повністю вираз і повернули останне значення.
console.log(result15)

var result16 = (+null == false) < 1 // false
// Спочатку виконується вираз у дужках і унарний + перетворює null на число 0
// Далі порівнюється 0 та false виходить true і це цифра 1
// потім порівнюється 1 менше 1 false
console.log(result16)

var result17 = false && true || true // true
// && повертає false, тому що обидва операнди повинні бути true
// далі порівнюється false та true і тут повертається true якщо хоч один операнд true
console.log(result17)

var result18 = false && (false || true) //false
// Спочатку виконується вираз у дужках і він повернає true
// після цього порівнюється false та true, повертається false тому-що обидва операнди повінні бути true
console.log(result18)

var result19 = (+null == false) < 1 ** 5 // false
// Спочатку виконується вираз що в дужках і він повертає 1 тому-що при порівняні значень повертається true
// Далі відбувається піднесення 1 до ступення 5 результат піднесення 1
// Далі порівнюється 1 з дужок та 1 яка була піднесенна до ступення 5 
// 1 не менше 1 тому повертає false 
console.log(result19)