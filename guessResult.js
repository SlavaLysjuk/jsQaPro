var result4 = +'40'+ +'2' + "hillel" // 42hillel
//унарний плюс завжди призводить до чісла, 
// виходить що стрічка 40 і 2 стаюсь числами потім між собою додаються 40 + 2 = 42 
// і далі канкатенуємо стрічку hillel, JS автоматично приводить всі числа у виразі до рядкових значень та виконeє конкатенацію
//якщо будь-яка з частин виразу є рядком
console.log(result4)

var result5 = '10' - 5 === 6; // false
//тут стрічка 10 перетворюється на число 10, потім віднимається від числа 5. 
// При строгому порівняні результат буде false, томущо 5 не дорінює 6.
console.log(result5)

var result6 = true + false // 1
// JS пробує завжди привести все до числа. true це 1 false 0.
// при додавані 1 + 0 результат 1.
console.log(result6)

var result7 = '4px' - 3 // NaN
